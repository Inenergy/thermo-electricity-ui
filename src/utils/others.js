const clone = (obj) => JSON.parse(JSON.stringify(obj));

const concat = (objects, names) => {
  const result = {};
  for (let i = 0; i < objects.length; i++) {
    for (let key in objects[i]) {
      result[key + names[i]] = clone(objects[i][key]);
    }
  }
  return result;
};

module.exports = { clone, concat };
